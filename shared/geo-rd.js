/**
 * Poblar un select con las provincias
 */
export function poblarProvincias(selectEl, placeholder = 'Selecciona la provincia...') {
  selectEl.innerHTML = `<option value="">${placeholder}</option>`;
  PROVINCIAS_RD.forEach(p => {
    const opt = document.createElement('option');
    opt.value = p.nombre;
    opt.textContent = p.nombre;
    selectEl.appendChild(opt);
  });
}

/**
 * Poblar un select con los municipios de una provincia
 */
export function poblarMunicipios(selectEl, provinciaNombre, placeholder = 'Selecciona el municipio...') {
  selectEl.innerHTML = `<option value="">${placeholder}</option>`;
  selectEl.disabled = !provinciaNombre;
  if (!provinciaNombre) return;

  const prov = PROVINCIAS_RD.find(p => p.nombre === provinciaNombre);
  if (!prov) return;

  const municipios = MUNICIPIOS_RD[prov.id] || [];
  municipios.forEach(m => {
    const opt = document.createElement('option');
    opt.value = m;
    opt.textContent = m;
    selectEl.appendChild(opt);
  });
}

/**
 * Poblar un select con los sectores de un municipio
 */
export function poblarSectores(selectEl, municipioNombre, placeholder = 'Selecciona el sector/barrio...') {
  selectEl.innerHTML = `<option value="">${placeholder}</option>`;
  selectEl.disabled = !municipioNombre;
  if (!municipioNombre) return;

  const sectores = SECTORES_RD[municipioNombre];
  if (!sectores?.length) {
    // Si no hay sectores específicos, habilitar para escritura libre
    selectEl.disabled = false;
    return;
  }

  sectores.forEach(s => {
    const opt = document.createElement('option');
    opt.value = s;
    opt.textContent = s;
    selectEl.appendChild(opt);
  });
}
